RunAction::RunAction(std::map<G4String, std::set<G4LogicalVolume*>> sensVols)
: fSensVols(sensVols)
{

  auto analysisManager = G4AnalysisManager::Instance();
  analysisManager->SetDefaultFileType("xml");
  analysisManager->SetVerboseLevel(1);

  // ID 0, Name: "TotalEdep", Title: "Total Energy Deposit per Event", 10000 bins from 0 to 10 MeV
  //analysisManager->CreateH1("TotalEdep", "Total Energy Deposit per Event", 10000, 0.0, 10.0);
  for ( const auto &entry : fSensVols ) {
    analysisManager->CreateH1(entry.first, "Total Energy Deposit per Event", 10000, 0.0, 10.0);
  }
}

